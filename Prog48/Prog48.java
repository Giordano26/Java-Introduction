//Fazer a contagem regressiva a partir de 20
//Até 0 quando então indica "FOGO!"

public class Prog48
{
    public static void main(String[] args)
    {
        for(int i = 20; i > 0; i--)
            System.out.println(i);
        System.out.println("FOGO!");
    }
}
