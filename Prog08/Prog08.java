public class Prog08
{
    public static void main(String[] args)
    {
    int A = 28/3;
    double B = 28/3;
    float C = 28/3;
    
    float D = 28/3f;
    double E = 28.0/3;
    double F = 28/3.0;
    
    System.out.println("int 28/3: " + A);
    System.out.println("double 28/3: " + B);
    System.out.println("float 28/3: " + C);
    System.out.println("float 28/3f: " + D);
    System.out.println("double 28/0.3: " + E);
    System.out.println("double 28/3.0: " + F);
    
}
}    